package com.example.healthybangladesh.programlogic;

public class Tip {
    private String tip;
    private String category;

    public Tip(String tip, String category) {
        this.tip = tip;
        this.category = category;
    }

    public String getTip() {
        return tip;
    }

    public String getCategory() {
        return category;
    }
}
